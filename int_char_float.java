public class int_char_float {
    public static void main(String[] args) {
        
        int integer = 7;
        char character = 'Z';
        float floats = 7.77F;

        System.out.println("Integer:"+integer);
        System.out.println("Character:"+character);
        System.out.println("Float:"+floats);
    }
    
}
